if answer == ("no"):#If the answer is no it will print the code below
    print ("\n")#Prints a blank line
    print ("You are correct.")#This will print if the answer is no
else:#If the user inputs an invalid variable
    print ("\n")#Prints a blank line
    print ("You are wrong.")#This prints if the user inputs an invalid variable
